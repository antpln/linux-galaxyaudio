// Load complete DSM firmware blob (913 bytes from 0x2050-0x23E0)
// Selects appropriate firmware based on speaker type (woofer vs tweeter)
static void max98390_load_dsm_firmware(struct max98390_hda_priv *priv, bool is_tweeter)
{
	const u8 *firmware;
	int i;

	// Select appropriate firmware blob
	if (is_tweeter) {
		firmware = max98390_dsm_firmware_tweeter;
	} else {
		firmware = max98390_dsm_firmware_woofer;
	}

	// Write all 913 bytes sequentially starting from 0x2050
	// This matches coolstar's uploadDSMBin() implementation
	for (i = 0; i < MAX98390_DSM_PARAM_SIZE; i++) {
		regmap_write(priv->regmap, MAX98390_DSM_START_ADDR + i, firmware[i]);
	}
}

// Configure DSM with appropriate firmware based on speaker type
// cutoff_freq: cutoff frequency in Hz (unused - filter built into tweeter firmware)
// is_tweeter: true for tweeters (0x3C, 0x3D), false for woofers (0x38, 0x39)
void max98390_configure_high_pass_filter(struct max98390_hda_priv *priv, int cutoff_freq, bool is_tweeter)
{
	// Load complete DSM firmware (913 bytes, all registers)
	// Tweeter firmware includes high-pass filter in STBASS section
	// Woofer firmware is full-range
	max98390_load_dsm_firmware(priv, is_tweeter);

	// When EQ8_EN is enabled, ALL 8 biquads must be configured!
	// Unused biquads must be set to PASSTHROUGH (B0=0x100000, others=0x000000)
	// Otherwise H(z) = 0/1 = 0 = NO SOUND!
	dev_info(priv->dev, "Configuring all 8 EQ biquads for EQ8_EN...\n");

	// Set ALL 8 biquads to passthrough first (B0=0x100000 = 1.0 in Q4.20)
	// BQ1: 0x2129-0x213B (use custom filter for tweeter later)
	// BQ2: 0x213D-0x214F
	// BQ3: 0x2151-0x2163
	// BQ4: 0x2165-0x2177
	// BQ5: 0x2179-0x218B
	// BQ6: 0x218D-0x219F
	// BQ7: 0x21A1-0x21B3
	// BQ8: 0x21B5-0x21C7
	for (int bq = 1; bq <= 8; bq++) {
		int base = 0x2129 + (bq - 1) * 0x14;  // Each biquad is 20 bytes (0x14)

		// B0 = 0x100000 (1.0 in Q4.20 = passthrough)
		regmap_write(priv->regmap, base + 0, 0x00);  // B0[7:0]
		regmap_write(priv->regmap, base + 1, 0x00);  // B0[15:8]
		regmap_write(priv->regmap, base + 2, 0x10);  // B0[23:16]
		// 0x2C reserved

		// B1, B2, A1, A2 = 0x000000 (all zeros for passthrough)
		for (int i = 4; i <= 18; i++) {
			if ((i - 3) % 4 != 0) {  // Skip reserved bytes (every 4th byte)
				regmap_write(priv->regmap, base + i, 0x00);
			}
		}
	}

	// For WOOFERS: Apply EasyEffects EQ using BQ1
	if (!is_tweeter) {
		// BQ1: 240.8 Hz, +22.91 dB, Q=4.65 (Bell/Peaking filter)
		// Converted from EasyEffects preset - Sample rate: 48000 Hz
		// Float coefficients: B0=1.011753, B1=-1.997196, B2=0.986436, A1=-1.997196, A2=0.998189
		// Q4.20 format (24-bit signed, LSB-first)

		int bq1_base = 0x2129;  // BQ1 starts at 0x2129

		// BQ1_B0 = 0x103024 (LSB-first: 0x24, 0x30, 0x10)
		regmap_write(priv->regmap, bq1_base + 0, 0x24);
		regmap_write(priv->regmap, bq1_base + 1, 0x30);
		regmap_write(priv->regmap, bq1_base + 2, 0x10);

		// BQ1_B1 = 0xE00B7C (LSB-first: 0x7C, 0x0B, 0xE0) - skip byte 3 (reserved)
		regmap_write(priv->regmap, bq1_base + 4, 0x7C);
		regmap_write(priv->regmap, bq1_base + 5, 0x0B);
		regmap_write(priv->regmap, bq1_base + 6, 0xE0);

		// BQ1_B2 = 0x0FC871 (LSB-first: 0x71, 0xC8, 0x0F) - skip byte 7 (reserved)
		regmap_write(priv->regmap, bq1_base + 8, 0x71);
		regmap_write(priv->regmap, bq1_base + 9, 0xC8);
		regmap_write(priv->regmap, bq1_base + 10, 0x0F);

		// BQ1_A1 = 0xE00B7C (LSB-first: 0x7C, 0x0B, 0xE0) - skip byte 11 (reserved)
		regmap_write(priv->regmap, bq1_base + 12, 0x7C);
		regmap_write(priv->regmap, bq1_base + 13, 0x0B);
		regmap_write(priv->regmap, bq1_base + 14, 0xE0);

		// BQ1_A2 = 0x0FF895 (LSB-first: 0x95, 0xF8, 0x0F) - skip byte 15 (reserved)
		regmap_write(priv->regmap, bq1_base + 16, 0x95);
		regmap_write(priv->regmap, bq1_base + 17, 0xF8);
		regmap_write(priv->regmap, bq1_base + 18, 0x0F);
	}

	// For tweeters: BQ1 with custom 3kHz high-pass filter
	if (is_tweeter) {
		// 2nd-order Butterworth HPF at 3kHz, 48kHz sample rate, Q=0.707
		// Coefficients in Q4.20 fixed-point format (24-bit):
		// b0 = 0.7570 → 0x0C1C2C
		// b1 = -1.5139 → 0xE7CE52 (two's complement)
		// b2 = 0.7570 → 0x0C1C2C
		// a1 = -1.4543 → 0xE8BC63 (two's complement)
		// a2 = 0.5741 → 0x092E0C

		// BQ1_B0 (0x2129-0x212B): [7:0], [15:8], [23:16]
		regmap_write(priv->regmap, 0x2129, 0x2C);
		regmap_write(priv->regmap, 0x212A, 0x1C);
		regmap_write(priv->regmap, 0x212B, 0x0C);

		// BQ1_B1 (0x212D-0x212F) - 0x212C is RESERVED, skip it!
		regmap_write(priv->regmap, 0x212D, 0x52);
		regmap_write(priv->regmap, 0x212E, 0xCE);
		regmap_write(priv->regmap, 0x212F, 0xE7);

		// BQ1_B2 (0x2131-0x2133) - 0x2130 is RESERVED, skip it!
		regmap_write(priv->regmap, 0x2131, 0x2C);
		regmap_write(priv->regmap, 0x2132, 0x1C);
		regmap_write(priv->regmap, 0x2133, 0x0C);

		// BQ1_A1 (0x2135-0x2137) - 0x2134 is RESERVED, skip it!
		regmap_write(priv->regmap, 0x2135, 0x63);
		regmap_write(priv->regmap, 0x2136, 0xBC);
		regmap_write(priv->regmap, 0x2137, 0xE8);

		// BQ1_A2 (0x2139-0x213B) - 0x2138 is RESERVED, skip it!
		regmap_write(priv->regmap, 0x2139, 0x0C);
		regmap_write(priv->regmap, 0x213A, 0x2E);
		regmap_write(priv->regmap, 0x213B, 0x09);
	}

	// Set DSM_VOL_CTRL - restored to firmware default (0xA0)
	regmap_write(priv->regmap, 0x23BA, 0xA0);

	// SET DSP_GLOBAL_EN TO 1 (WRITE 0x01 TO 0x23E1)
	regmap_write(priv->regmap, 0x23E1, 0x01);

	// SET SPK_EN TO 1 (WRITE 0x81 TO 0x203A)
	regmap_write(priv->regmap, 0x203A, 0x81);

	// SET EN TO 1 (WRITE 0x01 TO 0x23FF)
	regmap_write(priv->regmap, 0x23FF, 0x01);
}

